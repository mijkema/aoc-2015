def get_neighbors(i, j, size):
    res = []
    for x in (-1, 0, 1):
        for y in (-1, 0, 1):
            if x != 0 or y != 0:
                res.append((x + i, j + y))
    return res


def main(inp, is_real):
    inp = inp.strip()
    inp = inp.split('\n')
    on = set()
    size = len(inp)
    for i in range(size):
        row = inp[i]
        for j in range(size):
            if row[j] == '#':
                on.add((i, j))
    corners = [(0, 0), (size - 1, 0), (0, size - 1), (size - 1, size - 1)]
    on.update(corners)

    for turn in range(100 if is_real else 5):
        new_state = on.copy()
        for i in range(size):
            for j in range(size):
                turned_on = 0
                for n in get_neighbors(i, j, size):
                    if n in on:
                        turned_on += 1
                if (i, j) in on and (turned_on != 2 and turned_on != 3):
                    if (i, j) not in corners:
                        new_state.remove((i, j))
                elif turned_on == 3:
                    new_state.add((i, j))
        on = new_state
    print(len(on))


